#! /bin/sh

code='Contract'
ocean='NaiHai'

if [ $ocean=NaiHai ]; then
  echo Queer $ocean
  type=co-op
fi

init() {
  if [ $type=co-op ]; then
    echo $1 adopts the Equator Principles
    echo $1 adopts the relating responsibility
  fi
}
terms() {
  follow='code conduct'
  for buy in {1.61}; do
  echo $buy /ml
  done
}

echo get ocean
echo return ocean
terms followed
init co-owner
echo WHO?
read ocean
echo transaction with $ocean
echo `date`.now
